package lelysi.scalashop.model

case class User(email: Email, hashedPassword: String, paymentAccount: PaymentAccount)

case class UserLogin(email: Email, password: String)

case class Email(email: String) {
  def validateEmail(): Boolean = {
    // from https://emailregex.com/
    lazy val emailValidationRegEx =
      """(?:[a-z0-9!#$%&'*+/=?^_`{|}~-]+(?:\.[a-z0-9!#$%&'*+/=?^_`{|}~-]+)*|"(?:[\x01-\x08\x0b\x0c\x0e-\x1f\x21\x23-\x5b\x5d-\x7f]|\\[\x01-\x09\x0b\x0c\x0e-\x7f])*")@(?:(?:[a-z0-9](?:[a-z0-9-]*[a-z0-9])?\.)+[a-z0-9](?:[a-z0-9-]*[a-z0-9])?|\[(?:(?:25[0-5]|2[0-4][0-9]|[01]?[0-9][0-9]?)\.){3}(?:25[0-5]|2[0-4][0-9]|[01]?[0-9][0-9]?|[a-z0-9-]*[a-z0-9]:(?:[\x01-\x08\x0b\x0c\x0e-\x1f\x21-\x5a\x53-\x7f]|\\[\x01-\x09\x0b\x0c\x0e-\x7f])+)\])""".r
    emailValidationRegEx.matches(email)
  }

  require(validateEmail())

  override def toString: String = email
}

case class PaymentAccount(accountNumber: String)